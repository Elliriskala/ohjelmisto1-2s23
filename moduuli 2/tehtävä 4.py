num1 = int(input("Kirjoita yksi kokonaisluku: "))
num2 = int(input("Kirjoita toinen kokonaisluku: "))
num3 = int(input("Kirjoita kolmas kokonaisluku: "))
# lasketaan kokonaislukujen summa, tulo ja keskiarvo
print(f"Lukujen summa: {(num1 + num2 + num3)}")
print(f"Lukujen tulo: {(num1 * num2 * num3)}")
print(f"Lukujen keskiarvo: {((num1 + num2 + num3)/3):.1f}")

